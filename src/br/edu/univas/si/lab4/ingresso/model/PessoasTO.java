package br.edu.univas.si.lab4.ingresso.model;

/**
 * @author Mauro
 *
 */
public class PessoasTO {
	
	private String nome;
	private String cpf;
	private String cartaoCred;
	private String validadeCC;
	private String bandeiraCC;
	private String email;
	private String endereco;
	private String cep;
	private String cidade;
	private String estado;
	private String telefone;
	private String tipo;
	
	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}
	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}
	/**
	 * @return the cpf
	 */
	public String getCpf() {
		return cpf;
	}
	/**
	 * @param cpf the cpf to set
	 */
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	/**
	 * @return the cartaoCred
	 */
	public String getCartaoCred() {
		return cartaoCred;
	}
	/**
	 * @param cartaoCred the cartaoCred to set
	 */
	public void setCartaoCred(String cartaoCred) {
		this.cartaoCred = cartaoCred;
	}
	/**
	 * @return the validadeCC
	 */
	public String getValidadeCC() {
		return validadeCC;
	}
	/**
	 * @param validadeCC the validadeCC to set
	 */
	public void setValidadeCC(String validadeCC) {
		this.validadeCC = validadeCC;
	}
	/**
	 * @return the bandeiraCC
	 */
	public String getBandeiraCC() {
		return bandeiraCC;
	}
	/**
	 * @param bandeiraCC the bandeiraCC to set
	 */
	public void setBandeiraCC(String bandeiraCC) {
		this.bandeiraCC = bandeiraCC;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the endereco
	 */
	public String getEndereco() {
		return endereco;
	}
	/**
	 * @param endereco the endereco to set
	 */
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	/**
	 * @return the cep
	 */
	public String getCep() {
		return cep;
	}
	/**
	 * @param cep the cep to set
	 */
	public void setCep(String cep) {
		this.cep = cep;
	}
	/**
	 * @return the cidade
	 */
	public String getCidade() {
		return cidade;
	}
	/**
	 * @param cidade the cidade to set
	 */
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	/**
	 * @return the estado
	 */
	public String getEstado() {
		return estado;
	}
	/**
	 * @param estado the estado to set
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}
	/**
	 * @return the telefone
	 */
	public String getTelefone() {
		return telefone;
	}
	/**
	 * @param telefone the telefone to set
	 */
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	
	
}
